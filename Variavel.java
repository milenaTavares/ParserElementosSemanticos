public class Variavel {

    private String tipo;
    private String nome;
    private String valor;

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setValor(String nome) {
        this.valor = valor;
    }

    public String getTipo() {
        return this.tipo;
    }

    public String getNome() {
        return this.nome;
    }

    public String getValor() {
       return this.valor;
    }
}

